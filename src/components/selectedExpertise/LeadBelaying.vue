    />
    <br /><strong class="sub__heading">Gear Needed for Lead Belaying</strong>

    <br />Though a lead climber needs additional gear, like quickdraws and
    slings, your gear needs for lead belaying are the same as for top-rope
    belaying. As always, doublecheck with your class instructor to see what is
    required, but typically gear will be provided. <br />Any belay device for
    top-rope belaying is also appropriate for lead belaying. Note that belaying
    a lead climber is quite different between a tubular and a brake-assist
    device, so you need to learn and master the technique for your device. We
    cover lead belay with a tubular device in this article. <br /><br /><strong
      class="sub__heading"
      >Universal Belay Principles</strong
    >

    <br />Whether you’re belaying a top-rope climber or a lead climber, these
    principles remain unchanged:
    <br />
    <ul>
      <li>- Always keep your brake hand(s) on the rope. ALWAYS.</li>
      <br />
      <li>
        - Only slide a hand when the rope is firmly held in the braking
        position.
      </li>
      <br />
      <li>- Always orient brake hands in their strongest natural position.</li>
      <br />
      <li>
        - Always maintain attention on your climber, and be watchful for any
        hazards in your surroundings.
      </li>
    </ul>
    <br /><strong class="sub__heading">Setting Up the Belay</strong>

    <br />Your initial steps are the same as in setting up a top-rope belay:
    <br />
    <br />
    <ul>
      <li>The climber ties into the harness with a figure 8 knot.</li>
      <br />
      <li>
        You close the system by tying off the end of the rope with a stopper
        knot.
      </li>
      <br />
    </ul>
    <strong
      >When the climber is much heavier than you, also consider tying in to a
      ground anchor.</strong
    >
    Ground anchors are worth considering, too, whenever you’re forced to belay
    in a place where you have an obstruction between you and the wall.
    <br />
    <br /><strong>Unique Steps to Setting Up a Lead Belay</strong>

    <br />In top-rope climbing most of the rope is initially running up the wall
    or cliff to a top anchor and back down to the climber. In a lead belay,
    though, most of the rope is on the ground and the lead climber clips into
    bolts on the way up. Thus some steps differ from top-rope belay procedures:
    <br />
    <br />
    <ul>
      <li>
        - Double-check that your rope length is comfortably more than twice as
        long as the distance to the top anchor. If it’s shorter than this,
        you’ll run out of rope before the climber can be lowered all the way
        back to the ground. That would require you and your partner to set up a
        plan for top-site belaying instead.
      </li>
      <br />
      <li>
        - Set up your belay device the same as in a top-rope belay, but your
        attachment point along the rope is close to the climber. Be sure the
        climber has enough free rope, though, to be able to reach the first bolt
        along the route.
      </li>
      <br />
      <li>
        - Flake out the rope to make sure there aren’t any knots or kinks that
        might hang up as rope pays out through the belay device.
      </li>
    </ul>
    <br /><strong>Performing the Safety Check</strong> <br />Just as in top-rope
    belaying, the climber and belayer always double-check each other’s setup
    before climbing begins:
    <br />
    <ul>
      <li>
        <br />- <strong>Knots:</strong> Is the climber’s figure 8 tied correctly
        and did the belayer close the system correctly with a stopper knot at
        the end of the rope?
      </li>
      <br />
      <li>
        - <strong>Buckles:</strong> Are both harnesses snug with buckles
        securely fastened? Whether it’s inherent in the buckle design or (on
        older harnesses) the climber must perform the task, straps must double
        back through buckles to secure them.
      </li>
      <br />
      <li>
        - <strong>Belay Device:</strong> Is it properly threaded? Does the
        carabiner pass through the rope, belay-device cable and harness belay
        loop? Is the carabiner locked?
      </li>
      <br />
      <li>
        - <strong>Command Check:</strong> Because terms can vary, run through
        your communication to review terminology and to be sure you agree on
        every term you will use.
      </li>
    </ul>
    <br /><strong>Spotting the Climber</strong>

    <br />Climber spots another climber Belaying doesn’t begin until the climber
    clips into the first bolt. So you begin by spotting the climber during the
    ascent to that bolt. This bouldering skill requires you to adopt a balanced
    stance with arms up and fingers together. Focus on the center of the
    climber. If a fall occurs, your job is to keep the climber upright and
    ensure the person’s head doesn’t hit the ground.
    <br />
    <br />Before the climber begins the ascent to the first bolt, run through
    your initial belay commands:
    <br />
    <br />
    <ul>
      <li>- Climber: On Belay? &nbsp; Belayer: Belay On!</li>
      <br />

      <li>- Climber: Climbing! &nbsp; Belayer: Climb On!</li>
    </ul>
    <br /><strong>Getting in the Proper Belay Stance</strong>

    <br />Climber in correct belay stance Once the first bolt is clipped,
    quickly get ready to belay in earnest. You assume the standard belay stance,
    with your non-dominant foot forward, your knees bent and your whole body
    relaxed yet ready.
    <br />
    <br />Because climber falls can be far more forceful in lead belay than in
    top-rope belay, proper position is also more critical:
    <br />
    <br />
    <ul>
      <li>
        - You sometimes get yanked forcefully into the wall, so having a clear
        path to it is essential to ensure nothing trips you up.
      </li>
      <br />
      <li>
        - Also, don’t set up too far from the wall or you risk injury from
        impact. Even if you brace properly, an overly long swing into the wall
        can sprain an ankle.
      </li>
      <br />
      <li>
        - Finally, just as you do for top-rope belay, set up at least a step
        away from being directly under the climber’s route.
      </li>
      <br />
      <li>
        - After each clip point, be prepared to reposition yourself in alignment
        with the direction of pull should the climber fall.
      </li>
    </ul>

    <br /><strong>Belay Communication</strong> <br />Commands are the same as
    top-rope climbing, although a belayer hears “Slack!” far more frequently. As
    always, add your partner’s name to avoid confusion with other climbing
    teams. And shout loudly so your partner is certain about your command.
    <br /><br />
    <strong class="sub__heading">The Belay Process</strong>
    <br />
    Because you spend most of your time paying out rope, rather than taking in
    slack, your beginning hand placement and motions will differ from top-rope
    belaying. <br />
    <br />
    <ul>
      <li>
        - Grip the rope directly above the belay device with your guide hand.
      </li>
      <br />
      <li>
        - Slide your brake hand out away from the belay device and grip the
        rope.
      </li>
    </ul>
    <br />
    <strong>Lead Belay Technique</strong>
    <br />
    <ul>
      <li>
        - Give slack by feeding the rope up with your guide hand.
      </li>
      <br />
      <li>
        - Simultaneously, your brake hand gripping the rope will be pulled in
        close to the belay device
      </li>
      <br />
      <li>
        - Next, slide your guide hand back down so it’s directly above the
        device again; then regrip the rope.
      </li>

      <br />
      <li>
        - Then, slide your brake hand out away from the device and regrip the
        rope.
      </li>
    </ul>
    <br />

    Repeat the above sequence continuously, as long as the lead climber
    continues to ascend. <br /><br />
    <strong>Monitoring Rope Slack</strong>
    <br />
    correct amount of slack while belaying a climber Pay close attention to the
    amount of rope slack. Too little slack impedes progress and can pull a
    climber off the wall. Too much slack can set the climber up for a long and
    painful fall. Watch the amount of rope droop coming out of the belay device:

    <br /><br />
    <ul>
      <li>
        - If the rope is wire-straight out of the device, you have too little
        slack.
      </li>
      <br />
      <li>- If the rope droops below the device, you have too much slack.</li>
    </ul>
    <br />Just as you do for a top-rope belay, watch and listen closely to your
    climber’s progress and commands. When the climber pauses, you pause. When a
    climber yells “Clipping,” quickly pull rope up to provide slack. Do it
    faster by stepping forward as you work, but be careful not to lose your
    footing. <br /><br />
    <strong>Assessing Fall Risks</strong>
    <br />
    Because a leader falls twice as far as that climber has ascended above the
    last clip-in point, falls can be long and consequential. When a fall
    happens, you must react fast. As the climber is ascending you should be
    determining the risk of a fall at every point along the route:
    <br /><br />
    <ul>
      <li>
        - If an obstruction like a ledge or the ground is directly in the
        climber’s fall line, you’ll have to do a quick, hard catch (see below).
      </li>
      <br />
      <li>
        - If the climber will instead drop down from an overhang into free space
        or swing into the wall, you have to do a soft catch (see below).
      </li>
    </ul>
    <br />
    <strong>Making a Hard Catch </strong>
    <br />
    Whenever the climber is within falling distance of an obstruction, take a
    step or two closer to the wall so that you can more rapidly brake and hold.
    <br /><br />
    <ul>
      <li>
        - Fight the fall, performing a top-rope style catch by pulling your
        brake hand down hard while tightening your grip; your body, harness,
        rope and belay device must work together to absorb the force of the
        fall.
      </li>
      <br />
      <li>
        - The force of the fall is likely to be greater than a top-rope fall,
        though, so you’ll be pulled toward the wall or all the way up against
        it; be prepared to bend your knees and brace your feet against the wall.
      </li>
    </ul>
    <br />
    <strong>Making a Soft Catch</strong>
    <br />
    belayer catching a fall and lifting off the ground When the climber is not
    in peril of hitting an obstruction, stay put or step back to the original
    position so you’ll have room to maneuver as you get pulled toward the wall.
    You’ll be using your movement and the natural stretch of the rope to soften
    the effect of the fall when the climber bottoms out.
    <br /><br />
    <ul>
      <li>
        Pull your brake hand down and tighten your grip, just as you would to
        catch a top-rope fall.
      </li>
      <br />
      <li>
        Get ready to fly, especially if the fall is a long one—taking a quick
        hop toward the wall helps you direct where you’ll end up.
      </li>
      <br />
      <li>
        Let the fall’s force pull you into the air, keeping knees bent and feet
        forward to brace as you plant them against the wall, several feet off
        the ground.
      </li>
    </ul>
    <br />
    <strong class="sub__heading">Lowering a Climber</strong>
    <br />
    Not all lead climbers get down via this method, but lowering is a common
    practice. If that is the agreed-upon plan, the climber needs time, and might
    ask for some rope slack, in order to set this up. When ready, the climber
    yells, “tension!” After you remove all rope slack, you respond with
    “Gotcha!” The climber then leans back into a sitting position and yells,
    “Lower me!”
    <br /><br />
    <ul>
      <li>
        - Bring your guide hand under your brake hand.
      </li>
      <br />
      <li>- Keep both hands on the rope.</li>
      <br />
      <li>- Yell “Lowering!”</li>
      <br />
      <li>
        - Let the rope slowly feed through the belay device, lowering the
        climber.
      </li>
      <br />
      <li>
        - Maintain a steady pace, adjusting speed if the climber requests it.
      </li>

      <br />
      <li>
        - Slow up or stop when the climber nears a bolt and needs to clean
        (remove) the quickdraw; also slow up near obstructions and near the
        ground.
      </li>
    </ul>
    <br />
    When the climber is standing on the ground, safely balanced on two feet, the
    climber says, “off belay.”

    <br /><br />
    You respond by paying out plenty of slack and saying, “belay off.”
