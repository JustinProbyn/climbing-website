    If you’re a proficient top-rope belayer, you’re ready to learn how to belay
    a lead climber. The big difference between lead and top-rope belaying is
    that you spend most of your time paying out rope slack, rather than taking
    it in.
    <br />
    <br />Beyond slack management, you also have to master additional
    techniques. Catching lead falls, for example, is a skill that requires quick
    thinking and reflexes.
    <br />
    <br />Just as with top-rope belaying, gyms require you to pass a lead belay
    certification test, and climbing classes teach this skill as part of the
    lead climbing progression. This article covers lead belaying techniques, but
    is not meant to replace hands-on instruction.
    <br />
    <br />
    <img
      class="article__images"
