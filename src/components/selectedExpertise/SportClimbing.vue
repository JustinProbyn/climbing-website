    <strong>What it is</strong>
    <br />You hear the broad term sport climbing all the time, but its
    definition is quite specific.
    <br />
    <br />
    <em>
      <strong>Sport climbing:</strong>
    </em>
    <br />The act of climbing single- or multi-pitch routes, protected by
    permanently-fixed bolts and anchors drilled into the rock, using a rope and
    the aid of a belayer. The main difference between sport climbing and
    bouldering is the height of the routes being climbed and the form of
    protection (bouldering = no ropes, with crash pads). Likewise, traditional
    climbing calls for the use of temporary gear and anchors (ex. nuts,
    camalots, etc.) to protect the climber, as opposed to permanent ones used in
    sport climbing.
