    <strong>Before you go... &nbsp; find a mentor</strong>
    <br />This piece of advice applies to any form of rock climbing. As simple
    as climbing might seem, the technicalities and nuances are staggering. The
    best way to learn them is through the teachings of an experienced mentor.
