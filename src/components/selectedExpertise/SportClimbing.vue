    you’re not coming in blind; you probably already know someone who has the
    skills––you just need to ask them. If you’re lucky enough to score some time
    from this person, make sure you pay extremely close attention to his/her
    advice, and take it to heart. It’s critical to familiarise yourself with the
    basics of gear handling and safety precautions before you start blindly up a
    cliff.
