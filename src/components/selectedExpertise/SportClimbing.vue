      <br />

      <li>
        - Quickdraws: A quickdraw consists of two non-locking carabiners
        attached by webbing. This is the device you clip your rope into as you
        climb, and allows for some extra distance between the anchor (the bolt
        and bolt hanger) and the rope to reduce rope drag. Though a bit on the
        pricier side, the Petzl Spirit Express is a superb quickdraw.
      </li>
      <br />
      <li>
