    <strong>Crag etiquette</strong>

    <br />The way we behave at climbing areas is an oft overlooked
    consideration. Rock climbing is a highly individualised pursuit, but that
    doesn’t mean we get to ignore the experience of others. Everyone is trying
    to enjoy the crag as much as everyone else.
    <br />
    <br />Here are some things to keep in mind when it comes to respecting the
    area and your fellow climbers:
    <ul>
      <br />
      <li>
        - Keep it quiet. Avoid yelling, screaming, and loud music. Many climbers
        enjoy the peace and quiet of the crag, and don’t want to hear your crew
        conversing loudly over techno beats. The crag is not the gym!
      </li>
      <br />
      <li>
        - Consolidate your mess. Sport climbing requires a ton of gear, most of
        which somehow finds its way onto the ground. Keep your belongings
        consolidated in one area, out of the way of paths and belay stations.
      </li>
      <br />
      <li>
        - Trash sweep. Once you’re done packing up, sweep the area for trash. If
        you find something you left behind, pack it out. If you find something
        someone else left behind, pack it out as well. It’s our responsibility
        to keep our climbing areas clean.
      </li>
      <br />
      <li>
        - Play nice. Remember that many developed climbing areas are super
        popular. If you hate sharing the crag, consider finding a different area
        off the beaten path. If you do find yourself at a busy climbing area,
        respect your fellow climbers––don’t rush anyone to climb faster, don’t
        crowd a belayer’s space, and don’t be mean. Be someone you’d like to
        climb around!
      </li>
    </ul>
