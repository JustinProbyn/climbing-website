    <strong>How sport climbing works</strong>

    <br />Before you’re able to actually climb a sport route, the route needs to
    exist. A developer of a sport climb must first eye the potential line to be
    climbed. Next, s/he uses either a hand drill or a power drill (depending on
    local laws and regulations) to outfit the line with bolts and bolt hangers
    (read this post from Mountain Project for an overview of drill types and
    techniques).
    <br />
    <br />Many sport climbing areas feature single-pitch climbs. Generally, the
    term “pitch” refers to a route length that can be climbed and protected by a
    rope of average length, typically 60-70 meters. Multi-pitch sport climbs are
    common as well, especially in places with huge walls like Yosemite, and
    require the climber and belayer to climb the route pitch by pitch while
    anchored to the wall.
    <br />
    <br />The sport climbing process itself is relatively simple. A climber and
    a belayer tie into one end of a single rope, with a few meters of rope
    between them. As the climbers ascends the route, s/he clips into carabiners
    spaced intermittently up the wall, each of which act as anchors to protect
    the climber from falls. As the climber climbs, the belayer “feeds” her slack
    in the rope, and manually brakes any falls with the aid of a belay device.
